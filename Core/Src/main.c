void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == FLASH_USART_INSTANCE)
    {
        char int_msg = 0;
        uint16_t crc = ModRTU_CRC(receive_buffer, 18); // ignore last 2 bytes (CRC)
        uint16_t crc_rx = ((uint16_t)receive_buffer[19] << 8) + receive_buffer[18];

        // Check The Buffer Protocol is BLE RTU
        if (crc == crc_rx)
        {
            // Check The Function Code is 0xFF: Switch To Firmware Upgrade Mode.
            if (receive_buffer[0] == SLAVE_MOBILE)
            {
                // Use Progress To Handle IAP Command.
                int_msg = iap_progress(receive_buffer[1]);
                
                if (int_msg == 0x00)  // IAP Don't Send, So Send OK Response
                {
                    ble_rtu_message_packet(transmit_buffer, SLAVE_CATTRACKSOS, FNC_OK, NULL, 0);
                    HAL_UART_Transmit(&huart1, transmit_buffer, 20, HAL_MAX_DELAY);
                }
            }
        }
        else
        {
            // Send ERROR Response
            ble_rtu_message_packet(transmit_buffer, SLAVE_CATTRACKSOS, FNC_ERROR, NULL, 0);
            HAL_UART_Transmit(&huart1, transmit_buffer, 20, HAL_MAX_DELAY);
        }
    }
    
    // Wait Next Receive
    HAL_UART_Receive_IT(&huart1, receive_buffer, 20);
}
